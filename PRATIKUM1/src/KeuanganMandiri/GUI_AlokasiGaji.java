       public void insert(){
        Data3 out = new Data3();
        out.Bulan = (String) pilih.getSelectedItem();
        String a = out.Ctk_Bulan();
        String b = in.getText();
        out.input(Integer.parseInt(b));
        String c = String.valueOf(out.hitung());
        try {
            koneksi();
            Statement statement = conn.createStatement();
            statement.executeUpdate("INSERT INTO tb_riwayat(Bulan, Gaji, Sisa_Gaji)"+"VALUES('"+a+"','"+b+"','"+c+"')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data Matkul!");
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Input!");
        }
        refresh();
    }
       
            public void delete(){
      int ok=JOptionPane.showConfirmDialog(null,"Apakah Anda yakin akan menghapus data ?","Konfirmasi",JOptionPane.YES_NO_OPTION); if(ok==0){
          try{
              String sql="DELETE FROM tb_riwayat WHERE bulan='"+pilih.getSelectedItem()+"'";
              java.sql.PreparedStatement stmt=conn.prepareStatement(sql);
              stmt.executeUpdate();
              JOptionPane.showMessageDialog(null,"Data Berhasil dihapus");
          }
          catch(Exception e){
              JOptionPane.showMessageDialog(null,"Data gagal di hapus");
          }
          }
          refresh();
  }
       

